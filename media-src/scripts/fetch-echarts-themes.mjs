#!/usr/bin/env node
/*
 * fetch-echarts-themes — vendor a curated set of Apache ECharts' built-in gallery themes as
 * plain data, for the `vmarkd.theme.echarts` setting (task 90 follow-up).
 *
 * ECharts ships these themes as UMD files (`theme/<name>.js`) that call
 * `echarts.registerTheme(name, themeObject)`. Loading them as scripts would mean a per-theme
 * fetch + CSP exposure; instead we run each through an AMD shim here, capture the theme OBJECT,
 * and emit it as a bundled TS data module (`src/echarts-gallery.ts`) that the webview registers
 * itself — exactly how we register our derived `vmarkd` palette theme. Offline, no eval at runtime.
 *
 * Themes are Apache-2.0 (same as ECharts; LICENSE/NOTICE already vendored under vendor/echarts).
 *
 * Usage: node media-src/scripts/fetch-echarts-themes.mjs [<echartsVersion>]   (default 6.1.0)
 */
import { promises as fs } from 'node:fs'
import * as path from 'node:path'
import * as vm from 'node:vm'
import { fileURLToPath } from 'node:url'

// Curated, recognisable, visually distinct themes. The script keeps whichever the pinned ECharts
// version actually ships (others are skipped with a warning).
const WANTED = [
  'vintage',
  'macarons',
  'infographic',
  'roma',
  'shine',
  'tech-blue',
  'westeros',
  'essos',
  'wonderland',
  'walden',
  'chalk',
  'purple-passion',
  'halloween',
]

const OUT = path.resolve(
  path.dirname(fileURLToPath(import.meta.url)),
  '../../src/echarts-gallery.ts',
)

async function fetchText(url) {
  const res = await fetch(url, { headers: { 'User-Agent': 'vmarkd-fetch' } })
  if (!res.ok) return null
  return res.text()
}

// Run a UMD theme file through an AMD shim and capture the registered theme object.
function extractTheme(code, name) {
  let captured = null
  const echartsStub = {
    registerTheme: (_n, obj) => {
      captured = obj
    },
  }
  const sandbox = {
    define: Object.assign(
      (_deps, factory) => factory({}, echartsStub),
      { amd: true },
    ),
    module: undefined,
    exports: undefined,
  }
  vm.createContext(sandbox)
  vm.runInContext(code, sandbox, { filename: `${name}.js`, timeout: 5000 })
  return captured
}

async function main() {
  const version = process.argv[2] || '6.1.0'
  const themes = {}
  const kept = []
  for (const name of WANTED) {
    const code = await fetchText(
      `https://unpkg.com/echarts@${version}/theme/${name}.js`,
    )
    if (!code) {
      console.warn(`[themes] skip ${name} — not in echarts@${version}`)
      continue
    }
    const obj = extractTheme(code, name)
    if (!obj || typeof obj !== 'object') {
      console.warn(`[themes] skip ${name} — no theme object captured`)
      continue
    }
    // Round-trip through JSON to guarantee the object is pure data (no functions/regex).
    const json = JSON.parse(JSON.stringify(obj))
    themes[name] = json
    kept.push(name)
  }
  if (kept.length === 0) throw new Error('no themes captured')

  const body = `// GENERATED by media-src/scripts/fetch-echarts-themes.mjs — do not edit by hand.
//
// A curated set of Apache ECharts' built-in gallery themes (theme/<name>.js), captured as plain
// data so the webview can \`echarts.registerTheme\` them for the \`vmarkd.theme.echarts\` setting
// (task 90). Apache-2.0 — see media-src/vendor/echarts/{LICENSE,NOTICE}.
// Source: echarts@${version}/theme/*.js.

export const ECHARTS_GALLERY: Record<string, Record<string, unknown>> = ${JSON.stringify(themes, null, 2)}

/** The gallery theme ids (the \`theme.echarts\` values beyond auto / light / dark). */
export const ECHARTS_GALLERY_NAMES: readonly string[] = Object.keys(ECHARTS_GALLERY)
`
  await fs.writeFile(OUT, body)
  console.log(
    `[themes] wrote ${kept.length} themes → src/echarts-gallery.ts: ${kept.join(', ')}`,
  )
  console.log('Remember to update the package.json enum + tasks/90 + CHANGELOG.')
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
